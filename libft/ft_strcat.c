#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *tmp;

	tmp = s1;
	if (*s2 != 0)
	{
		while (*tmp)
			tmp++;
		while (*s2)
		{
			*tmp = *s2;
			tmp++;
			s2++;
		}
		*tmp = 0;
	}
	return (s1);
}
